package logica;

public class Controlador {
    
    public double sumar(double a, double b) {
        
        return a + b;
    }
    
    public double restar(double a, double b) {
        
        return a - b;
    }
    
    public double multipicar(double a, double b) {
        
        return a * b;
    }
    
    public double dividir(double a, double b) {
   
        return a / b;
    }
    
}
